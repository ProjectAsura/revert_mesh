#pragma once

//-----------------------------------------------------------------------------
// Includes
//-----------------------------------------------------------------------------
#include <cstdint>
#include <vector>
#include <string>


class CsvParser;

///////////////////////////////////////////////////////////////////////////////
// Float2 structure
///////////////////////////////////////////////////////////////////////////////
struct Float2
{
    float x;
    float y;
};

///////////////////////////////////////////////////////////////////////////////
// Float3 structure
///////////////////////////////////////////////////////////////////////////////
struct Float3
{
    float x;
    float y;
    float z;
};

///////////////////////////////////////////////////////////////////////////////
// Float4 structure
///////////////////////////////////////////////////////////////////////////////
struct Float4
{
    float x;
    float y;
    float z;
    float w;
};

///////////////////////////////////////////////////////////////////////////////
// Uint4 structure
///////////////////////////////////////////////////////////////////////////////
struct Uint4
{
    uint32_t x;
    uint32_t y;
    uint32_t z;
    uint32_t w;
};

///////////////////////////////////////////////////////////////////////////////
// Mesh structure
///////////////////////////////////////////////////////////////////////////////
struct Mesh
{
    std::string             Name;
    uint32_t                MaterialId;
    std::vector<Float3>     Positions;
    std::vector<Float3>     Normals;
    std::vector<Float4>     Tangents;
    std::vector<Float4>     Colors;
    std::vector<Float2>     TexCoords[4];
    std::vector<Uint4>      BoneIndices[2];
    std::vector<Float4>     BoneWeights[2];
};

///////////////////////////////////////////////////////////////////////////////
// Material structure
///////////////////////////////////////////////////////////////////////////////
struct Material
{
    std::string             Name;
};

///////////////////////////////////////////////////////////////////////////////
// Model structure
///////////////////////////////////////////////////////////////////////////////
struct Model
{
    std::vector<Mesh>        Meshes;
    std::vector<Material>    Materials;
};
